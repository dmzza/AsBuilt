import type { VisionAvailability, VisionStatus } from "./types";

/**
 * Derive a review-facing vision status from score notes + dim counts.
 * Notes are the source of truth for skipped/failed vision passes.
 */
export function deriveVisionStatus(opts: {
  notes: string[];
  referenceDimCount: number;
  candidateDimCount: number;
  usedReferenceGold: boolean;
  usedCandidateGold: boolean;
}): VisionStatus {
  const { notes } = opts;
  const details = notes.filter((n) =>
    /vision|api_key|anthropic|openai|extract failed|topology/i.test(n),
  );

  const missingKey = notes.some(
    (n) =>
      /No ANTHROPIC_API_KEY or OPENAI_API_KEY/i.test(n) ||
      /No vision client — skipped vision dim/i.test(n),
  );
  const disabled = notes.some((n) => /Vision disabled \(useVision=false\)/i.test(n));
  const providerNote = notes.find((n) => /Vision provider:/i.test(n));
  const providerMatch = providerNote?.match(/Vision provider:\s*(\S+)\s*\/\s*(\S+)/i);
  const extractFailed = notes.some((n) => /extract failed/i.test(n));
  const topologySkippedNoClient = notes.some((n) =>
    /No vision client — skipped topology/i.test(n),
  );
  const bothGold = opts.usedReferenceGold && opts.usedCandidateGold;
  const anyProposedDims = opts.referenceDimCount + opts.candidateDimCount > 0;

  if (disabled) {
    return {
      availability: "disabled",
      label: "AI disabled",
      summary: "Vision was turned off for this run (useVision=false). Dimension proposals were not generated by AI.",
      details,
    };
  }

  if (missingKey) {
    return {
      availability: "missing_key",
      label: "No API key",
      summary:
        "AI was not used — set ANTHROPIC_API_KEY or OPENAI_API_KEY and re-score to get proposed dimensions and topology findings.",
      details,
    };
  }

  if (bothGold && !providerNote) {
    return {
      availability: "gold_only",
      label: "Gold dims",
      summary: topologySkippedNoClient
        ? "Scoring used verified gold dimensions. Topology vision was skipped (no API key)."
        : "Scoring used verified gold dimensions (no live AI dim extract).",
      details,
    };
  }

  if (topologySkippedNoClient && !providerNote && !anyProposedDims) {
    return {
      availability: "missing_key",
      label: "No API key",
      summary:
        "AI was not used — set ANTHROPIC_API_KEY or OPENAI_API_KEY and re-score to get proposed dimensions and topology findings.",
      details,
    };
  }

  if (providerNote) {
    const provider = providerMatch?.[1];
    const model = providerMatch?.[2];
    if (extractFailed && !anyProposedDims) {
      return {
        availability: "failed",
        label: "AI failed",
        summary: `AI ran (${provider ?? "vision"} / ${model ?? "?"}) but dimension extraction returned nothing usable. Check notes and re-score.`,
        provider,
        model,
        details,
      };
    }
    if (extractFailed) {
      return {
        availability: "partial",
        label: "AI partial",
        summary: `AI ran (${provider ?? "vision"} / ${model ?? "?"}) but some extract passes failed — review proposed dims carefully.`,
        provider,
        model,
        details,
      };
    }
    return {
      availability: "used",
      label: model ? `AI · ${model}` : "AI used",
      summary: `AI vision used (${provider ?? "vision"} / ${model ?? "model"}).`,
      provider,
      model,
      details,
    };
  }

  if (!anyProposedDims && !bothGold) {
    return {
      availability: "failed",
      label: "No AI dims",
      summary: "No dimension readings available — AI did not run or produced no proposals. Re-score with an API key.",
      details,
    };
  }

  return {
    availability: "used",
    label: "AI / gold",
    summary: "Dimension readings present.",
    details,
  };
}

export function visionStatusTone(
  availability: VisionAvailability,
): "ok" | "warn" | "bad" {
  if (availability === "used") return "ok";
  if (
    availability === "partial" ||
    availability === "disabled" ||
    availability === "gold_only"
  ) {
    return "warn";
  }
  return "bad";
}
